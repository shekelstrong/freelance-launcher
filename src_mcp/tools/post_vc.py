"""post_vc: генерация статьи для VC.ru."""


async def run(topic: str, angle: str = "опыт", length: str = "medium") -> dict:
    """Генерирует статью для VC.ru.

    Args:
        topic: Тема.
        angle: Угол подачи (опыт / кейс / инструкция / аналитика).
        length: short / medium / long.

    Returns:
        Словарь с заголовком, подзаголовками, телом, тегами.
    """
    lengths = {"short": 800, "medium": 1500, "long": 3000}
    word_count = lengths.get(length, 1500)

    title = f"{topic}: что я понял за последние 3 года"

    subtitles = [
        "Контекст и зачем это нужно",
        "Главная ошибка, которую делают все",
        "Как я делаю сейчас",
        "Что попробовать в первую очередь",
        "Что дальше",
    ]

    body_template = f"""# {title}

{subtitles[0]}

{topic} — тема, которая всплывает снова и снова. В этом посте я расскажу свой подход.

{subtitles[1]}

Главная ошибка — пытаться сделать идеально с первого раза. В реальности работает итеративный подход: сделал → измерил → поправил → повторил.

{subtitles[2]}

Мой текущий процесс:
- Шаг 1: быстрый MVP
- Шаг 2: замер метрик
- Шаг 3: фидбек от реальных пользователей
- Шаг 4: итерация

{subtitles[3]}

Что попробовать в первую очередь:
1. Начни с малого
2. Замерь результат
3. Не бойся менять

{subtitles[4]}

Если у вас есть вопросы — пишите в комментариях, разберу детально."""

    return {
        "title": title,
        "angle": angle,
        "length": length,
        "word_count_target": word_count,
        "subtitles": subtitles,
        "body": body_template,
        "tags": ["опыт", "кейс", topic.split()[0].lower()],
        "best_posting_time": "Вторник-четверг, 10:00-12:00 МСК",
    }
